// Filter coupons by type
export const getCouponsByType = (type: string) => {
  if (type === 'all') {
    return mockCoupons;
  } else if (type === 'active') {
    return mockCoupons.filter(coupon => coupon.status === 'Active');
  } else if (type === 'archived') {
    return mockCoupons.filter(coupon => coupon.status === 'Inactive');
  }
  return mockCoupons;
};

// Get counts for each type
export const getCouponCounts = () => {
  return {
    all: mockCoupons.length,
    active: mockCoupons.filter(coupon => coupon.status === 'Active').length,
    archived: mockCoupons.filter(coupon => coupon.status === 'Inactive').length,
  };
};

